package logistics.wms.beans;

public class CustodianBean {

	private String nameCustodian;
	private int noDept;
	private String nameDept;
	
	public String getNameCustodian() {
		return nameCustodian;
	}
	public void setNameCustodian(String nameCustodian) {
		this.nameCustodian = nameCustodian;
	}
	public int getNoDept() {
		return noDept;
	}
	public void setNoDept(int noDept) {
		this.noDept = noDept;
	}
	public String getNameDept() {
		return nameDept;
	}
	public void setNameDept(String nameDept) {
		this.nameDept = nameDept;
	}
}
